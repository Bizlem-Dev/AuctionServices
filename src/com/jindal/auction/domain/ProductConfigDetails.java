package com.jindal.auction.domain;

public class ProductConfigDetails {
	
	private String configId;
	private String configItemId;
	private int SeqNo;
	private String configOptionId;
	private String productId;
	private Double quantity;
	private String productName;
	private String uomId;
	private String uomName;
	private String categoryName;
	
	
	
	
	public String getConfigId() {
		return configId;
	}
	public void setConfigId(String configId) {
		this.configId = configId;
	}
	public String getConfigItemId() {
		return configItemId;
	}
	public void setConfigItemId(String configItemId) {
		this.configItemId = configItemId;
	}
	public int getSeqNo() {
		return SeqNo;
	}
	public void setSeqNo(int seqNo) {
		SeqNo = seqNo;
	}
	public String getConfigOptionId() {
		return configOptionId;
	}
	public void setConfigOptionId(String configOptionId) {
		this.configOptionId = configOptionId;
	}
	public String getProductId() {
		return productId;
	}
	public void setProductId(String productId) {
		this.productId = productId;
	}
	public Double getQuantity() {
		return quantity;
	}
	public void setQuantity(Double quantity) {
		this.quantity = quantity;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getUomId() {
		return uomId;
	}
	public void setUomId(String uomId) {
		this.uomId = uomId;
	}
	public String getUomName() {
		return uomName;
	}
	public void setUomName(String uomName) {
		this.uomName = uomName;
	}
	public String getCategoryName() {
		return categoryName;
	}
	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}
	
	
	

}
